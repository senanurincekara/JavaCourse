package javaCourse;

public class exercise3 {

	public static void main(String[] args) {
		// FOR
		
				for (int  i=1; i < 10; i++) {
					System.out.println("*");
					
				}
			
				
				// WHİLE
				
				int i= 1;
				while (i<10) {
					System.out.println(i);
					i+=3;
				}
				
				
				// DO-WHİLE
				
				int j =0;
				do {
					
					System.out.println(j);
					j+=2;
					
				}while(j<10);

	}

}
